    <h2>Présentation du plugin Téléchargement Icône WooCommerce</h2>
    <p>Ce plugin ajoute une icône de téléchargement aux produits téléchargeables sur votre site WooCommerce. Il permet aux utilisateurs de facilement identifier les produits téléchargeables.</p>

    <h3>Fonctionnalités clés :</h3>
    <ul>
        <li>Affiche une icône de téléchargement sur les produits téléchargeables.</li>
        <li>Personnalisez le style de l'icône avec du CSS.</li>
    </ul>

    <p>Pour activer le plugin, assurez-vous d'activer WooCommerce et téléchargez-le dans la section des plugins de votre site WordPress.</p>
</div>
